#include <iostream>
#include <sstream>
#include <cstdio>
#include <vector>
#include <math.h>
using namespace std;

int main()
{
    int r,n;
    double ans;
    scanf("%d %d",&r,&n);
    while(r!=0)
    {
            ans=sin(acos(-1)/(2.0*n));
            ans=r/ans;
            printf("%0.2f\n",ans);
            scanf("%d %d",&r,&n);
    }
}
